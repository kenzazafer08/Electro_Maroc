function show(){
    var more = document.getElementById("show-more");
    var newp = document.getElementById("new");
    more.style.display = "inline";
    newp.style.display = "none";
}
function hide(){
    var more = document.getElementById("show-more");
    var newp = document.getElementById("new");
    more.style.display = "none";
    newp.style.display = "inline";
}